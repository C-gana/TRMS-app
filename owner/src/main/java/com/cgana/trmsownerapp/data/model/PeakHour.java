package com.cgana.trmsownerapp.data.model;

public class PeakHour {
    private int hour;
    private int journeys;

    // Getters and Setters
    public int getHour() {
        return hour;
    }

    public void setHour(int hour) {
        this.hour = hour;
    }

    public int getJourneys() {
        return journeys;
    }

    public void setJourneys(int journeys) {
        this.journeys = journeys;
    }
}

